/*
 * Concepts Covered:
 * 1. Class
 * 2. main() Method
 * 3. Comments
 * 4. Output Statements
 * 5. Program Execution Flow
 */

public class HelloWorld {

    /*
     * main() is the entry point of a Java application.
     *
     * Analogy:
     * Think of a house.
     * The class is the house,
     * and main() is the front door.
     *
     * Whenever the program starts,
     * Java enters through this door first.
     */
    public static void main(String[] args) {

        /*
         * System.out.println()
         *
         * System  -> Java's built-in system utilities
         * out     -> Standard output stream (console)
         * println -> Print a line and move to the next line
         */

        System.out.println("Hello, World!");

    }
}
